
def poly_add2(L1, L2):
   x = (L1[0] + L2[0])
   y = (L1[1] + L2[1])
   z = (L1[2] + L2[2])
   return [x,y,z]

def poly_mult2(L1, L2):
   x_4 = (L1[0] * L2[0])
   x_3 = (L1[0] * L2[1]) + (L1[1] * L2[0])
   x_2 = (L1[1] *L2[1]) + (L1[0] * L2[2]) + (L1[2] * L2[0])
   x = (L1[1] * L2[2]) + (L1[2] * L2[2])
   y = (L1[2] * L2[2])
   return [x_4, x_3, x_2, x, y]
